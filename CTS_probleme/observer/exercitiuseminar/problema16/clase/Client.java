package observer.exercitiuseminar.problema16.clase;

public class Client {
	private String nume;
	private String andresaMail;
	private String numarTelefon;
	public Client(String nume, String andresaMail, String numarTelefon) {
		super();
		this.nume = nume;
		this.andresaMail = andresaMail;
		this.numarTelefon = numarTelefon;
	}
	public String getNume() {
		return nume;
	}
	public String getAndresaMail() {
		return andresaMail;
	}
	public String getNumarTelefon() {
		return numarTelefon;
	}
	
	

}
